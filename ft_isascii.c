#include <stdio.h>

int	ft_isascii(int x)
{
	if (x >= 0 && x <= 127)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
int	main(void)
{
	printf("%d", ft_isascii('a'));
}

