#include <stdio.h>

int	ft_isalpha(int x)
{
	if ((x >= 'A' && x <= 'Z') || (x >= 'a' && x <= 'z'))
		return (0);
	return (1);
}

int	main(void)
{
	char	a;

	a = 'A';
	printf("%d", a);
}
