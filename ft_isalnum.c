#include <stdio.h>

int	ft_isalpha(int x)
{
	if ((x >= 'A' && x <= 'Z') || (x >= 'a' && x <= 'z') || (x >= '0'
			&& x <= '9'))
	{
		return (1);
	}
	else
	{
		return (0);
	}

}

int	main(void)
{
	int	a;

	a = 'b';
	printf("%d", ft_isalpha('a'));
}
