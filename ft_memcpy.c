#include <stdio.h>
#include <string.h>

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t				i;
	unsigned char		*d;
	const unsigned char	*s;

	i = 0;
	d = (unsigned char *)dest;
	s = (const unsigned char *)src;
	while (i < n)
	{
		d[i] = s[i];
		i++;
	}
	return (dest);
}
int	main(void)
{
	char	src[] = "ABC";
	char	dest[4];

	ft_memcpy(dest, src, 4);
	printf("%s\n", dest);
	return (0);
}
